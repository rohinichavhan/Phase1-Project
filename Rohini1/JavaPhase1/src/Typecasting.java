
public class Typecasting {
	
	    public static void main(String[] args) {
	       String str = "100";
	       System.out.println(Integer.valueOf(str) + ": This is a Integer Value");
	       System.out.println(Boolean.valueOf(str) + ": This is a Boolean Value");
	       System.out.println(Long.valueOf(str) + ": This is a Long  Value");
	       System.out.println(Float.valueOf(str) + ": This is a Float  Value");
	       System.out.println(Double.valueOf(str) + ": This is a Double Value");
	       System.out.println(Short.valueOf(str) + ": This a is Short Value");
	       System.out.println(Byte.valueOf(str) + ": This is a Byte Value");

	    }
	}